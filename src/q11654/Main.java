package q11654;

import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		
		
		Scanner sc = new Scanner(System.in);
		
		//charAt 의 경우는 문자열을 캐릭터형으로 한글자를 추출하는 것이다. 안에 0은 인덱스를 뜻함 
	    char word = sc.next().charAt(0);
	    
	    System.out.println((int) word);//숫자형으로 바꿔주면 아스키코드 출력
		
		
		
		
		
	}

}
